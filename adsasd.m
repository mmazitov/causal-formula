# Formula Builder

## 🎯 About

Formula Builder is a web application that allows users to create and evaluate mathematical formulas using a combination of variables and operators. The project is built using modern web technologies including React, TypeScript, Vite, and Tailwind CSS.

## 🛠 Tech Stack

- **[React](https://react.dev/):** A JavaScript library for building user interfaces.
- **[TypeScript](https://www.typescriptlang.org/):** A programming language that adds static typing to JavaScript.
- **[Vite](https://vite.dev/):** A build tool that provides a faster and leaner development experience for modern web projects.
- **[Tailwind CSS](https://tailwindcss.com/):** A utility-first CSS framework for styling.
- **[Zustand](https://zustand-demo.pmnd.rs/):** A small, fast, and scalable state-management solution.
- **[React Query](https://react-query.tanstack.com/):** A library for fetching, caching, and updating asynchronous data in React.
- **[ESLint](https://eslint.org/):** A tool for identifying and fixing problems in JavaScript code.
- **[Prettier](https://prettier.io/):** An opinionated code formatter.

# 🚀 Features

- **Formula Input:** Users can type variables and operators to build formulas.
- **Autocomplete Suggestions:** Provides suggestions for variables as users type.
- **Formula Display:** Shows the current formula and its calculated result.
- **Tag Management:** Allows users to add, remove, and modify variable tags.
- **State Management:** Uses Zustand for state management.
- **Data Fetching:** Uses React Query to fetch autocomplete suggestions from an API.

## 🏁 Starting

```
Before starting 🏁, you need to have [Git](https://git-scm.com) and [Node](https://nodejs.org/en/) installed.

# Clone this project
$ git clone https://github.com/mmazitov/causal-formula

# Access
$ cd causal-formula

# Install the dependencies using Yarn or npm:
$ yarn install or $ npm install

#To start the development server, run:
$ npm run dev or $ yarn dev

# To build the project for production, run:
$ npm run build or $ yarn build

# To preview the production build, run:
$ npm run preview or $ yarn preview

# Lint project
$ npm lint or $ yarn lint


```

